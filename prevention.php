<p>Mając na&nbsp;uwadze aktualnie obowiązujące prawo, tj.&nbsp;art.
22b ustawy z&nbsp;13&nbsp;maja 2016&nbsp;r. o&nbsp;przeciwdziałaniu
zagrożeniom przestępczością na&nbsp;tle seksualnym i&nbsp;ochronie
małoletnich (Dz.&nbsp;U. z&nbsp;2023&nbsp;r. poz. 1304) oraz
art.&nbsp;7 pkt.&nbsp;6 ustawy z&nbsp;dnia 28&nbsp;lipca 2023&nbsp;r.
o&nbsp;zmianie ustawy Kodeks rodzinny i&nbsp;opiekuńczy oraz
niektórych innych ustaw (Dz.&nbsp;U. z&nbsp;2023&nbsp;r. poz. 1606)
<strong>Proboszcz Parafii Rzymskokatolickiej pod wezwaniem Podwyższenia
Krzyża Świętego w&nbsp;Sandomierzu z&nbsp;dniem 15&nbsp;sierpnia
2024&nbsp;r. wprowadza do&nbsp;stosowania „Standardy Ochrony
Małoletnich”</strong>, których podstawowym celem jest zapewnienie
bezpieczeństwa małoletnim wiernym parafii, dbałość o&nbsp;ich dobro,
uwzględnianie ich potrzeb i&nbsp;podejmowanie działań w&nbsp;ich jak
najlepszym interesie.</p><br>

