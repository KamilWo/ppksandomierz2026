        Historia Parafii...
    </div>
    <div class="col">
        <blockquote>
            <p class="text-indent">rozpoczyna się w&nbsp;1983&nbsp;r., kiedy to ówczesny ks. kan.
                Stanisław
                Bartoszewski - proboszcz Parafii św.&nbsp;Pawła zdecydował, za aprobatą władzy
                diecezjalnej, aby
                u&nbsp;zbiegu ulic Ożarowskiej i&nbsp;Mickiewicza powstał punkt duszpasterski. Po
                ofiarowaniu
                przez Pana Bolesława Woźniaka placu, ówczesny Pasterz Diecezji, 22&nbsp;maja 1983&nbsp;r.
                poświęcił plac i&nbsp;krzyż, przy którym odprawił Mszę świętą. Od tej pory rozpoczęło
                się
                przygotowanie do budowy najpierw tymczasowej kaplicy, a&nbsp;później kościoła. Wykonanie
                projektu świątyni zlecono Pani architekt inżynier Zofii Łotkowskiej z&nbsp;Sandomierza,
                obliczenia konstrukcyjne inżynierowi Mieczysławowi Frańczakowi. Pracami kierował ksiądz
                Antoni
                Firmanty.</p>

            <p class="text-indent">Rozpoczęcie budowy po wielu trudnościach nastąpiło 15&nbsp;maja 1987&nbsp;r.
                Od początku września 1991&nbsp;r. do pracy w&nbsp;tej parafii został skierowany przez
                Biskupa
                Ordynariusza Edwarda Materskiego ks.&nbsp;Józef Śmigiel, który kontynuował budowę. On
                też
                powołał Radę Parafialną. Prace budowlane posuwały się naprzód dzięki wysiłkowi Rady,
                która
                gromadziła środki pieniężne od parafian oraz dzięki osobistej pracy fizycznej większości
                parafian, którzy wyznaczani przez Radnych, przychodzili do budowy.</p>

            <p class="text-indent">Dzięki ofiarności i&nbsp;zaangażowaniu Parafian 17&nbsp;czerwca 1992&nbsp;r.
                odbyła się uroczystość wmurowania kamienia węgielnego, poświęconego przez Ojca Świętego
                Jana
                Pawła II. Eucharystię Wielkiego Czwartku w&nbsp;1994 r. odprawiono już w&nbsp;nowej
                świątyni,
                chociaż nie było jeszcze okien i&nbsp;drzwi. W&nbsp;czerwcu 1996&nbsp;r. Biskup Marian
                Zimałek
                poświęcił dzwony, które zachęcają do modlitwy Anioł Pański i&nbsp;wzywają na Apel
                Jasnogórski o&nbsp;godz.
                21. Na Boże Narodzenie 1997&nbsp;r. był już ołtarz i&nbsp;posadzka w&nbsp;prezbiterium.
                W&nbsp;styczniu
                1998&nbsp;r. wykonano posadzkę w&nbsp;całym kościele, jesienią tegoż roku ogrzewanie,
                wymalowana
                też została przez krakowskich artystów Droga Krzyżowa - którą w&nbsp;1999 r. poświęcił
                Biskup
                Marian Zimałek.</p>

            <p class="text-indent">We wrześniu 2000&nbsp;r. zostały zakupione organy, zamontowane
                tabernakulum,
                wykonany został także witraż św.&nbsp;Brata Alberta - patrona parafii. W&nbsp;niszy
                ołtarza
                złożono relikwie św.&nbsp;Siostry Faustyny. Relikwie
                św. Brata Alberta znajdują się w&nbsp;przenośnym krzyżu stojącym na ołtarzu. 17&nbsp;września
                2000&nbsp;r., w&nbsp;roku Wielkiego Jubileuszu, Biskup Marian Zimałek dokonał
                uroczystego
                poświęcenia świątyni.</p>
        </blockquote>
